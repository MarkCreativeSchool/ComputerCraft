# ===================
# コンピュータクラフト
# 基本操作系
# ===================

# 前に進む
turtle.forward()

# 後ろに戻る
turtle.back()

# 右に回転
turtle.turnRight()

# 左に回転
turtle.turnLeft()

# 上に移動
turtle.up()

# 下に移動
turtle.down()

# 前を掘る
turtle.dig()

# 上を掘る
turtle.digUp()

# 下を掘る
turtle.digDown()

# 繰り返し
for i=1,16 do
  turtle.forward()
end
